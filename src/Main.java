public class Main {

    public static void main(String[] args) {
        // example 1
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // example 2
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);

        //example 3
        int j = 1;
        while (j <= 10) {
            System.out.println(j);
            j++;
        }
    }
}
